import random  
import time

def rs():
    """rs chooses a random step and returns it.
       Note that a call to rs() requires parentheses.
       Arguments: none at all!
    """
    return random.choice([-1, 1])


def rwpos(start, nsteps):
    """ rwpos models a random walker that
        * starts at the int argument, start
        * takes the int # of steps, nsteps

        rwpos returns the final position of the walker.
    """
    time.sleep(0.1)
    print('location is', start)
    if nsteps == 0:
        return start
    else:
        newpos = start + rs()  # take one step
        return rwpos(newpos, nsteps - 1)

def rwsteps(start, low, hi):
    """ rwsteps models a random walker which
        * is currently at start 
        * is in a walkway from low (usually 0) to hi (max location) 
          
        rwsteps returns the # of steps taken 
        when the walker reaches an edge

        A MONKEY ESCAPED AND IS RUNNING AROUND. IT GOT INTO THE ALCHOOL SO
        KEEP A SAFE DISTANCE AND JUST WATCH IT MOVE
    """
    walkway = "_"*(hi-low)    # create a walkway of underscores
    S = (start-low)           # this is our sleepwalker's location, start-low

    walkway = walkway[:S] + "ğŸ¦" + walkway[S:]  # put our sleepwalker, "S", there

    walkway = "WALL" + walkway + "WALL"              # surround with spaces, for now...

    print(walkway, "    ", start, low, hi)     # print everything to keep track...
    time.sleep(0.05)

    if start <= low or start >= hi:            # base case: no steps if we're at an endpt
        print("MONEKY CRASHED INTO WALL START LAUGHING HAHAHHAHAHAHHAHHAHHAHAHAHHAHAHAHAH")
        return 0
    
    else:
        newstart = start + rs()                # takes one step, from start to newstart
        return 1 + rwsteps(newstart, low, hi)  # counts one step, recurses for the rest!

def bumperCars(cA, cB):
    """
    cA:starting position of car 1
    cB:starting position of car 2
    This simulator observes two bumper cars, cA and cB, driving
    till they bump into each other
    the cars always move till they bump
    low will always be 0
    high always 42
    """
    low = 0
    high = 50
    walkway = "_"*(high-low)    # create a walkway of underscores
    A = (cA-low)           # this is our sleepwalker's location, start-low
    B = (high - cB)

    walkway = walkway[:A] + "ğŸš˜" + walkway[A:B] + "ğŸš”" + walkway[B:] # put our sleepwalker, "S", there

    walkway = "WALL" + walkway + "WALL"              # surround with spaces, for now...

    print(walkway, "    ", cA, cB, low, high)     # print everything to keep track...
    time.sleep(0.05)

    if cA >= cB:            # base case: end if cars match up on spot
        print("CRASH")
        return 0
    elif cA <= low:
        newstartA = cA + 1                
        newstartB = cB + rs()
        return 1 + bumperCars(newstartA, newstartB)
    elif cB >= high:
        newstartA = cA + rs()                
        newstartB = cB - 1
        return 1 + bumperCars(newstartA, newstartB)
    else:
        newstartA = cA + rs()                
        newstartB = cB + rs()
        return 1 + bumperCars(newstartA, newstartB)  # counts one step, recurses for the rest!
    
